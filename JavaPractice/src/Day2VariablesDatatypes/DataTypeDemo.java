package Day2VariablesDatatypes;

public class DataTypeDemo {

	public static void main(String[] args) {
		// Numeric datatypes
		int a=100,b=200;
		System.out.println(a);//100
		System.out.println(b);//200
		
		byte by=120;
		System.out.println(by);//120
		
		short sh=250;
		System.out.println(sh);//250
		
		long l=123456789011121314l;//literal l is needed at end of value
		System.out.println(l);//123456789011121314
		
		//decimal data types
		float price=123.56f;
		System.out.println(price);//123.56
		
		double item_price=1234567.89;
		System.out.println(item_price);//1234567.89
		//character
		char grade='A';
		System.out.println("Grade:"+grade);//Grade:A
		
		//boolean
		boolean bool=false;
		System.out.println(bool);//false
		
		//String
		String name="Jhansi";
		System.out.println(name);//Jhansi
		
//		char z="B";//invalid
		
		
		
		

	}

}
