package AccessModifiers.Pack1;

public class Test1 {
	//Private
	private int w=100;
	private void m1() {
		System.out.println("This is m1 method.........");
	}
	
	//default
	
	int x=100;
	void m2() {
		System.out.println("This is m2 method.........");
	}
	
	
	//protected
	protected int y=100;
	protected void m3() {
	System.out.println("This is Protected m3 method.........");
	}
	
	
	
	//public
	public int z=100;
	public int x1;
	public void m4() {
	System.out.println("This is m4 method.........");
	}
	
	

}
